{-# LANGUAGE NoImplicitPrelude #-}

-- ----------------------------------------------------------------------------
-- Module declaration
-- ----------------------------------------------------------------------------

module Functor.Covariant
  (
    Covariant(..)
  ) where

-- ----------------------------------------------------------------------------
-- Covariant type class
-- ----------------------------------------------------------------------------

class Covariant f where
  fmap :: (a -> b) -> (f a -> f b)

-- ----------------------------------------------------------------------------
-- List instance for Covariant
-- ----------------------------------------------------------------------------

instance Covariant [] where
  fmap _ []     = []
  fmap f (x:xs) =  f x : fmap f xs
